"""Interactive first-time setup for yc-plugin.

Asks user for:
  - Path to OAuth client_secret.json from Google Cloud Console
  - Default privacy / category / tags (optional)

Writes .env to plugin data dir.

Usage:
  python setup.py                    # interactive
  python setup.py --client-secret PATH [--privacy unlisted] [--tags tag1,tag2]
                                     # non-interactive (for slash command)
"""
import argparse
import io
import os
import sys
from pathlib import Path

# Force UTF-8 output on Windows cp950 console.
if sys.stdout.encoding and sys.stdout.encoding.lower() not in ("utf-8", "utf8"):
    sys.stdout = io.TextIOWrapper(sys.stdout.buffer, encoding="utf-8", errors="replace")
    sys.stderr = io.TextIOWrapper(sys.stderr.buffer, encoding="utf-8", errors="replace")


def plugin_data_dir() -> Path:
    env = os.environ.get("CLAUDE_PLUGIN_DATA")
    if env:
        p = Path(env)
    else:
        p = Path.home() / ".claude" / "plugins" / "data" / "yc-plugin"
    p.mkdir(parents=True, exist_ok=True)
    return p


SETUP_INSTRUCTIONS = """
yc-plugin first-time setup
============================

You need an OAuth client_secret JSON from Google Cloud Console.
Steps (一次性，~3 分鐘):

  1. 打開 https://console.cloud.google.com/
  2. 建一個 project（任意名字，例如 "yc-plugin"）
  3. 左側選單 APIs & Services → Library
     → 搜 "YouTube Data API v3" → 點 Enable
  4. APIs & Services → OAuth consent screen
     → 選 External → Create
     → 填 App name (任意)、User support email (你)、Developer contact email (你)
     → 下一步 (Scopes 不用加)
     → Test users 點 Add Users → 加你自己的 Gmail
     → 完成
  5. APIs & Services → Credentials → + Create Credentials → OAuth client ID
     → Application type: Desktop app
     → Name 任意 → Create
     → 點 DOWNLOAD JSON 下載
     → 把這個 JSON 檔案放到你想要的位置（例如 ~/Documents/youtube_client_secret.json）

拿到 JSON 檔案後，把它的「絕對路徑」貼到下面。
"""


def write_env(data_dir: Path, fields: dict):
    env_file = data_dir / ".env"
    cs_path = fields["client_secret"].replace("\\", "/")  # normalize to forward slashes
    lines = [
        "# yc-plugin / youtube-upload settings (auto-generated by setup.py)",
        "# Edit by re-running setup.py or by hand.",
        "",
        f"YOUTUBE_CLIENT_SECRET_PATH={cs_path}",
        f"YOUTUBE_DEFAULT_PRIVACY={fields.get('privacy', 'unlisted')}",
        f"YOUTUBE_DEFAULT_CATEGORY={fields.get('category', '22')}",
        f"YOUTUBE_DEFAULT_TAGS={fields.get('tags', '')}",
        f"YOUTUBE_CHANNEL_NAME={fields.get('channel_name', '')}",
    ]
    env_file.write_text("\n".join(lines) + "\n", encoding="utf-8")
    return env_file


def validate_client_secret(path: Path) -> tuple[bool, str]:
    if not path.exists():
        return False, f"檔案不存在: {path}"
    if path.is_dir():
        return False, f"是個目錄不是檔案: {path}"
    try:
        import json
        data = json.loads(path.read_text(encoding="utf-8"))
        if "installed" not in data and "web" not in data:
            return False, "JSON 格式不對 — 缺 'installed' 或 'web' key。請確認下載的是 OAuth client (Desktop app) credentials。"
        return True, "OK"
    except Exception as e:
        return False, f"無法解析 JSON: {e}"


def interactive():
    print(SETUP_INSTRUCTIONS)

    while True:
        try:
            raw = input("client_secret.json 絕對路徑: ").strip().strip('"').strip("'")
        except (EOFError, KeyboardInterrupt):
            print("\n中斷 setup.")
            sys.exit(1)
        if not raw:
            print("不能空白，再試一次。")
            continue
        path = Path(raw).expanduser()
        ok, msg = validate_client_secret(path)
        if ok:
            client_secret = str(path.resolve())
            break
        print(f"  [X] {msg}\n")

    privacy = input("預設隱私 (public/unlisted/private) [unlisted]: ").strip() or "unlisted"
    if privacy not in ("public", "unlisted", "private"):
        print(f"  [X] 不認識 '{privacy}'，用預設 unlisted")
        privacy = "unlisted"

    tags = input("預設 tags (逗號分隔，可留空): ").strip()
    channel_name = input("頻道名稱 label (可留空): ").strip()

    return {
        "client_secret": client_secret,
        "privacy": privacy,
        "tags": tags,
        "channel_name": channel_name,
    }


def main():
    p = argparse.ArgumentParser()
    p.add_argument("--client-secret", help="Absolute path to client_secret.json")
    p.add_argument("--privacy", choices=["public", "unlisted", "private"], default="unlisted")
    p.add_argument("--category", default="22")
    p.add_argument("--tags", default="")
    p.add_argument("--channel-name", default="")
    p.add_argument("--show", action="store_true", help="Print current .env and exit")
    args = p.parse_args()

    data_dir = plugin_data_dir()

    if args.show:
        env_file = data_dir / ".env"
        if env_file.exists():
            print(f"# {env_file}")
            print(env_file.read_text(encoding="utf-8"))
        else:
            print(f"no .env at {env_file}")
        return

    if args.client_secret:
        path = Path(args.client_secret).expanduser()
        ok, msg = validate_client_secret(path)
        if not ok:
            sys.exit(f"client_secret invalid: {msg}")
        fields = {
            "client_secret": str(path.resolve()),
            "privacy": args.privacy,
            "category": args.category,
            "tags": args.tags,
            "channel_name": args.channel_name,
        }
    else:
        fields = interactive()

    env_file = write_env(data_dir, fields)
    print(f"\n[OK] 設定已寫入: {env_file}")
    print("第一次跑 /youtube-upload 時會跳出瀏覽器要 Google 登入授權，授權後 token 會存著，之後不會再問。")


if __name__ == "__main__":
    main()
